package com.timetable.DataModels;

public class Room {

	private String room_no;
	private int id;
	
	public String getRoom_no() {
		return room_no;
	}
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public void setRoom_no(String room_no) {
		this.room_no = room_no;
	}
	
}
