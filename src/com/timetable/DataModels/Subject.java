package com.timetable.DataModels;

public class Subject {

    private String subject_id;
    private String subject_name;
    private double l_credits;
    private double t_credits;
    private double p_credits;
	public String getSubject_id() {
		return subject_id;
	}
	public void setSubject_id(String subject_id) {
		this.subject_id = subject_id;
	}
	public String getSubject_name() {
		return subject_name;
	}
	public void setSubject_name(String subject_name) {
		this.subject_name = subject_name;
	}
	public double getL_credits() {
		return l_credits;
	}
	public void setL_credits(double l_credits) {
		this.l_credits = l_credits;
	}
	public double getT_credits() {
		return t_credits;
	}
	public void setT_credits(double t_credits) {
		this.t_credits = t_credits;
	}
	public double getP_credits() {
		return p_credits;
	}
	public void setP_credits(double p_credits) {
		this.p_credits = p_credits;
	}
    
    
	
}
